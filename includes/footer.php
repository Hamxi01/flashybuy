///================ Show items From Cart ======================= ///

function showCart(){
  
  $.ajax({
    type:"post",
    url:"ajax_Cart.php",
    data:{},
    success:function(data){
      
      var data = data.split("`");
      $('#ps-cart__items').html(data[0]);
      $('#total_cart_items').html(data[1]);

       var val = $('#total_cart_items').html();
       
        if (val == 0) {

            $('#ps-cart__items').css('display','none');
        }else{

            $('#ps-cart__items').css('display','');
        }
      
    }
  });
}

///================ Remove An item From Cart ======================= ///

function remove_cart(p_id){



    $.ajax({
        type:"post",
        url:"ajax_Cart.php",
        data:{action:'delete',p_id:p_id},
        success:function(data){

            var data = data.split("`");
            $('#ps-cart__items').html(data[0]);
            $('#total_cart_items').html(data[1]);
            if (data[1] == 0) {

            $('#ps-cart__items').css('display','none');
            }else{

                $('#ps-cart__items').css('display','');
            }
        }
    });
}
